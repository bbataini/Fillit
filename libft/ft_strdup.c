#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*cpy;

	i = 0;
	while (s[i])
		i++;
	if (!(cpy = (char*)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		cpy[i] = s[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
